package com.hirerachy;

public class NumberOfRecords {

	public static void main(String[] args) {
		
		int numberofRecords;
		
		String supervisor="supervisor";
		
		System.out.println("for");
		for(numberofRecords=10 ; numberofRecords>=0 ; --numberofRecords){
			
			System.out.println("for");
			System.out.println(supervisor + String.valueOf(numberofRecords));
			
		}
		

	}

}
